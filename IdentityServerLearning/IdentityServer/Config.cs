            {
                new IdentityResources.OpenId(),
                new IdentityResources.Profile(),
                new IdentityResource(CustomIdentityResourceName, "Custom Identity Resource",
                    new List<string>{"customidentity-claim1","customidentity-claim2"})
            };

        public static IEnumerable<ApiResource> Apis =>
           new List<ApiResource>
           {
               new ApiResource(TrialApiName, "My API"),
               new ApiResource("api2", "My API 2"),
               new ApiResource("api3", "My API 3")
           };

        public static IEnumerable<Client> Clients =>
            new List<Client>
            {
                new Client
                {
                    ClientId = "client",
                    ClientSecrets = { new Secret("secret".Sha256()) },
                    AllowedScopes = {"api1"},
                    AllowedGrantTypes = GrantTypes.ClientCredentials
                },
                new Client
                {
                    ClientId = "mvc",
                    ClientSecrets = { new Secret("secret".Sha256())},
                    AllowedScopes = new List<string>
                    {
                        IdentityServerConstants.StandardScopes.OpenId,
                        IdentityServerConstants.StandardScopes.Profile,
